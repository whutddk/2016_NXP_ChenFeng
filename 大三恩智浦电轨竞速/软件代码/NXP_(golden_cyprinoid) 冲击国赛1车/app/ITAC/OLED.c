//输出汉字字符串
void OLED_P14x16Str(uint8 x,uint8 y,uint8 ch[])
{
  uint8 wm=0,ii = 0;
  uint16 adder=1;
  
  while(ch[ii] != '\0')
  {
    wm = 0;
    adder = 1;
    while(F14x16_Idx[wm] > 127)
    {
      if(F14x16_Idx[wm] == ch[ii])
      {
        if(F14x16_Idx[wm + 1] == ch[ii + 1])
        {
          adder = wm * 14;
          break;
        }
      }
      wm += 2;
    }
    if(x>118){x=0;y++;}
    OLED_Set_Pos(x , y);
    if(adder != 1)// 显示汉字
    {
      OLED_Set_Pos(x , y);
      for(wm = 0;wm < 14;wm++)
      {
        OLED_WrDat(F14x16[adder]);
        adder += 1;
      }
      OLED_Set_Pos(x,y + 1);
      for(wm = 0;wm < 14;wm++)
      {
        OLED_WrDat(F14x16[adder]);
        adder += 1;
      }
    }
    else			  //显示空白字符
    {
      ii += 1;
      OLED_Set_Pos(x,y);
      for(wm = 0;wm < 16;wm++)
      {
        OLED_WrDat(0);
      }
      OLED_Set_Pos(x,y + 1);
      for(wm = 0;wm < 16;wm++)
      {
        OLED_WrDat(0);
      }
    }
    x += 14;
    ii += 2;
  }
}
//输出汉字和字符混合字符串
void OLED_Print(uint8 x, uint8 y, uint8 ch[])
{
  uint8 ch2[3];
  uint8 ii=0;
  while(ch[ii] != '\0')
  {
    if(ch[ii] > 127)
    {
      ch2[0] = ch[ii];
      ch2[1] = ch[ii + 1];
      ch2[2] = '\0';			//汉字为两个字节
      OLED_P14x16Str(x , y, ch2);	//显示汉字
      x += 14;
      ii += 2;
    }
    else
    {
      ch2[0] = ch[ii];
      ch2[1] = '\0';			//字母占一个字节
      OLED_P8x16Str(x , y , ch2);	//显示字母
      x += 8;
      ii+= 1;
    }
  }
}
